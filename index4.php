      <h1>Resultados Del Sistema De Protección Contra La Sequía</h1>
        <p>La protección contra la sequía, se basa en un sensor que detecta la escasez de humedad en el suelo, al detectarla, éste abre un grifo que regará las plantas hasta alcanzar una humedad con la que puedan sobrevivir. Cuando alcancen esta humedad, los sensorees la detectarán y cerrarán los sistemas de riego<br>
            Teniendo activa la protección contra la sequía hemos podido registrar un total de 7 activaciones en 2 semanas, lo cual nos ha servido para evitar que las plantas se sequen y no pasen a ser merma. </p>
